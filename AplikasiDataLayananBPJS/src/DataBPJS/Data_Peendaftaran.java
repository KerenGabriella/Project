private void tampilData(){
        int row = table_data.getRowCount();
        for(int a = 0 ; a < row ; a++){
            table.removeRow(0);
        }
        String query = "Select * from `pendaftaran`";
        
        try{
            Connection conn = koneksi.getKoneksi();//memanggil koneksi
            Statement st = conn.createStatement();//membuat statement
            ResultSet rs = st.executeQuery(query);//menjalankan query
            
            while(rs.next()){
            
                String nik = rs.getString("nik");
                String nama = rs.getString("nama");
                String alamat = rs.getString("alamat");
                String faskes= rs.getString("faskes");

            //masukan semua data kedalam array
            String[] pendaftaran = {nik,nama,alamat,faskes};
            //menambahakan baris sesuai dengan data yang tersimpan diarray
            table.addRow(pendaftaran);
            }
            table_data.setModel(table);
        }catch(Exception e){
            System.out.println("e");
        }
    }
