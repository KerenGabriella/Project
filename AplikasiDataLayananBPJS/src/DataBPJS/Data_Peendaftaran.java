    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = table_data.getSelectedRow();
        LayananPendaftaran menu = new LayananPendaftaran();
        menu.setVisible(true);
        String nik = table.getValueAt(row, 0).toString();
        menu.nik_txt.setText(nik);
        String nama = table.getValueAt(row, 1).toString();
        menu.nama_txt.setText(nama);       
        String alamat = table.getValueAt(row, 2).toString();
        menu.alamat_txt.setText(alamat);
        if("KLS1".equals(table_data.getValueAt(row, 3).toString())){
            kls1_rb.setSelected(true);
        }else if ("KLS2".equals(table_data.getValueAt(row, 3).toString())) {
            kls2_rb.setSelected(true);
        }else if("KLS3".equals(table_data.getValueAt(row, 3).toString())) {
            kls3_rb.setSelected(true);
        }
       
        menu.setVisible(true);
        menu.pack();
        menu.setDefaultCloseOperation(LayananPendaftaran.DISPOSE_ON_CLOSE);
        this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String jdbc_driver = "com.mysql.jdbc.Driver";
    String user = "root";
    String pass = "";
    String url = "jdbc:mysql://localhost:3306/db_bpjs";
    File reportFile = new File(".");
    String dirr = "";
        try {
            Class.forName(jdbc_driver);
            Connection conn = (Connection) DriverManager.getConnection(url, user, pass);
            Statement stat = conn.createStatement();
            String sql = "SELECT * FROM pendaftaran";
            dirr = reportFile.getCanonicalPath()+"/src/DataBPJS/";
            JasperDesign design = JRXmlLoader.load(dirr + "pendaftaran.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(design);
            ResultSet rs = stat.executeQuery(sql);
            JRResultSetDataSource rsDataSource = new JRResultSetDataSource(rs);
            JasperPrint jp = JasperFillManager.fillReport(jr, new HashMap(), rsDataSource);
            JasperViewer.viewReport(jp);
        } catch (ClassNotFoundException | SQLException |IOException |JRException ex) {
            JOptionPane.showMessageDialog(null, "\nGagal Mencetak\n" + ex,
                    "Kesalahan" , JOptionPane.ERROR_MESSAGE);
                    
        } 
    
    }//GEN-LAST:event_jButton1ActionPerformed

